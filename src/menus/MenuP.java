    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String input = JOptionPane.showInputDialog("Ingrese la cantidad de ambulancias (deben ser mayor a 3 y menor que 6:");

        try {
            
            int cantidadAmbulancias = Integer.parseInt(input);

            
            if (cantidadAmbulancias > 3 && cantidadAmbulancias < 6) {
                ArrayList<Ambulancia> asituar = new ArrayList<>();
                for (int i = 0; i < cantidadAmbulancias; i++) {
                    String placa = JOptionPane.showInputDialog("Digite la placa de la Ambulancia número " + i+1 + ": ");
                    Ambulancia amb = new Ambulancia(placa);
                    asituar.add(amb);
                }
                conexBarrios.situar(asituar);
                R_repaintEli();
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un número válido de ambulancias.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un número válido.");
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        Graphics g = jPanel1.getGraphics();
        int numVer = conexBarrios.barrios.orden();

        String input = JOptionPane.showInputDialog(null, "Ingrese la posicion del vertice donde hubo un accidente\n(mayor igual que 0 y menor o igual a " + (numVer-1) + "");
        try {
    
            int numeroVertices = Integer.parseInt(input);
            
            if (numeroVertices >= 0 &&  numeroVertices <= numVer) {
               
                int posIni = conexBarrios.enviarAmbulanciaMasCerca(numeroVertices);
                System.out.println(posIni);
                Floyd1 reco = new Floyd1(conexBarrios.barrios);
                    if (reco.f[posIni][numeroVertices] != conexBarrios.barrios.infinito()) {
                        reco.ruta(posIni, numeroVertices);
                        LinkedList<Integer> pos = reco.mrut;
                        pos.addFirst(posIni);
                        pos.addLast(numeroVertices);
                        for (int i = 1; i < pos.size(); i++) {
                            int x1 = conexBarrios.corX.get(pos.get(i-1));
                            int y1 = conexBarrios.corY.get(pos.get(i-1));

                            int x2 = conexBarrios.corX.get(pos.get(i));
                            int y2 = conexBarrios.corY.get(pos.get(i));

                            ver.pintarCamino(g, x1, y1, x2, y2, Color.yellow);
                        }
                        conexBarrios.barrios.obtVertice(numeroVertices).getAccidentesReportados().add(new Accidente("Tal/tal/tal", "Por tal tal tal.."));
                        conexBarrios.conAmbulancia.get(posIni).getAccidentesReportados().add(new Accidente("Tal/Tal/Tal", "Por tales y tales motivos..."));
                    }
                else{
                    JOptionPane.showMessageDialog(null, "El barrio esta aislado");     
                }
                    
                JOptionPane.showConfirmDialog(null, "¿Deseas continuar?", "Confirmación", JOptionPane.YES_NO_OPTION);
                R_repaintEli();
                
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, ingrese un número válido en el rango especificado.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un número válido.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    public int buscar(String n){
        for (int i = 0; i < conexBarrios.barrios.orden(); i++) {
            if (n.equalsIgnoreCase(conexBarrios.barrios.obtVertice(i).getNombre())) {
                return i;
            }
        }
        return -1;
    }
    
    
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Graphics g = jPanel1.getGraphics();
        int numVer = conexBarrios.barrios.orden();

        String input = JOptionPane.showInputDialog(null, "Ingrese la posicion del vertice de donde quiere que la ambulancia\nHaga el recorrido (mayor a 0 menor a 32)");
        try {
    
            int numeroVertices = Integer.parseInt(input);
            
            if (numeroVertices >= 0 &&  numeroVertices <= numVer) {
                
                
                Profundidad prof = new Profundidad();
                prof.dfs(conexBarrios.barrios,numeroVertices);
                ArrayList<Integer> pos = new ArrayList<>();
                for (String visitado : prof.visitados) {
                    int posi = buscar(visitado);
                    pos.add(posi);
                }
                 
                
                for (int i = 1; i < pos.size(); i++) {
                    int x1 = conexBarrios.corX.get(pos.get(i-1));
                    int y1 = conexBarrios.corY.get(pos.get(i-1));

                    int x2 = conexBarrios.corX.get(pos.get(i));
                    int y2 = conexBarrios.corY.get(pos.get(i));

                    ver.pintarCamino(g, x1, y1, x2, y2, Color.yellow);
                }
                
                
                    
                 JOptionPane.showConfirmDialog(null, "¿Deseas continuar?", "Confirmación", JOptionPane.YES_NO_OPTION);
                 R_repaintEli();
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, ingrese un número válido en el rango especificado.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un número válido.");
        }
        
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

