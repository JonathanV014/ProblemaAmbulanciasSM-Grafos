    private void generarAristaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarAristaActionPerformed
        int numVer = conexBarrios.barrios.orden();
        int maximoVertices = (numVer-1)*numVer;

        String input = JOptionPane.showInputDialog(null, "Ingrese el número de aristas aleatorios (mayor que 0 y menor o igual a " + maximoVertices + "):");
        
        try {
    
            int numeroVertices = Integer.parseInt(input);

            if (numeroVertices > 0 && numeroVertices <= maximoVertices) {
                conexBarrios.eliminarTodasAristas();
                R_repaintEli();
                conexBarrios.geneArisGrafo(conexBarrios.barrios, numeroVertices);
               
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, ingrese un número válido en el rango especificado.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un número válido.");
        }
        
  
    }//GEN-LAST:event_generarAristaActionPerformed
