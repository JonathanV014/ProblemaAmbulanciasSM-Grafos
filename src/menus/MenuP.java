    private void ejecucion() {
        if (mostrarAristas || mostrarVertices) {
            Thread procesoThread = new Thread(() -> {
            while (isRunning) {
                pintar();
                
                try {
                    Thread.sleep(1000); 
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
                }
            });
            procesoThread.start();
        }
    }

    
 
    private void pintar() {
        
        Graphics g = jPanel1.getGraphics();
        if (g != null) {
            if (mostrarVertices) {
                for (int i = 0; i < conexBarrios.barrios.orden(); i++) {
                int x = conexBarrios.corX.get(i);
                int y = conexBarrios.corY.get(i);
                Ambulancia ambu = conexBarrios.barrios.obtVertice(i).getTieneAmbu();
                if(ambu==null){
                    ver.pintarCirculo(g, x, y, "" + i); 
                }else{
                    ver.clickSobreNodo(g, x, y, ""+i, Color.red);    
                }}
                mostrarVertices = false;
            }
            
            if (mostrarAristas) {
                for (int i = 0; i < conexBarrios.barrios.orden(); i++) {
                    for (int j = 0; j < conexBarrios.barrios.orden(); j++) {
                        int x1 = conexBarrios.corX.get(i);
                        int y1 = conexBarrios.corY.get(i);

                        int x2 = conexBarrios.corX.get(j);
                        int y2 = conexBarrios.corY.get(j);
  
                        if (i != j && conexBarrios.barrios.obtArista(i, j) != conexBarrios.barrios.infinito() ) {
                            float tam = conexBarrios.barrios.obtArista(i, j );
                            ver.pintarLinea(g, x1, y1, x2, y2, tam);
                        }
                    }
                }
                mostrarAristas = false;
                mostrarVertices = true;
            }
        }
    }
    
    
    
